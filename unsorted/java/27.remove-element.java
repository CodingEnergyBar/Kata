// @lc code=start
class Solution {
    public int removeElement(int[] nums, int val) {
        int len = nums.length;
        int i = 0, k = 0; // [0...k)不等于 val 的
        for (; i < nums.length; i++) {
            if (nums[i] != val) {
                nums[k++] = nums[i];
            }
        }
        return k;
    }
}
